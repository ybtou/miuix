/**
 * Bakes the squircle SDF into a generated `BakedSquircleSdf` object so the runtime skips the
 * first-paint distance-field sweep. Bytes are emitted as a chunked Base64 literal (each chunk
 * under the JVM 65535-byte constant-pool entry limit). Re-runs when any input changes.
 */
abstract class BakeSquircleSdfTask : DefaultTask() {
    @get:Input abstract val control: Property<Float>

    @get:Input abstract val size: Property<Int>

    @get:Input abstract val halfRange: Property<Float>

    @get:Input abstract val bezierSamples: Property<Int>

    @get:Input abstract val packageName: Property<String>

    @get:OutputDirectory abstract val outputDir: DirectoryProperty

    @TaskAction
    fun bake() {
        val sz = size.get()
        val ctrl = control.get()
        val hr = halfRange.get()
        val bs = bezierSamples.get()
        val pkg = packageName.get()

        val bytes = generateSdfBytes(sz, ctrl, hr, bs)
        val base64 = Base64.encode(bytes)
        // 60000 < JVM CONSTANT_Utf8 limit (65535) AND divisible by 4 (Base64 alignment).
        val chunks = base64.chunked(60000)

        val pkgDir = outputDir.get().asFile.resolve(pkg.replace('.', '/'))
        pkgDir.mkdirs()
        val file = pkgDir.resolve("BakedSquircleSdf.kt")
        val source =
            buildString {
                appendLine("// Copyright 2026, compose-miuix-ui contributors")
                appendLine("// SPDX-License-Identifier: Apache-2.0")
                appendLine()
                appendLine("// Auto-generated by :miuix-squircle:bakeSquircleSdf -- do not edit.")
                appendLine()
                appendLine("@file:OptIn(kotlin.io.encoding.ExperimentalEncodingApi::class)")
                appendLine()
                appendLine("package $pkg")
                appendLine()
                appendLine("import kotlin.io.encoding.Base64")
                appendLine()
                appendLine("internal object BakedSquircleSdf {")
                appendLine("    const val CONTROL: Float = ${ctrl}f")
                appendLine("    const val SIZE: Int = $sz")
                appendLine("    const val HALF_RANGE: Float = ${hr}f")
                appendLine()
                appendLine("    private val CHUNKS: Array<String> = arrayOf(")
                for (chunk in chunks) {
                    appendLine("        \"$chunk\",")
                }
                appendLine("    )")
                appendLine()
                appendLine("    val bytes: ByteArray by lazy(LazyThreadSafetyMode.PUBLICATION) {")
                appendLine("        Base64.decode(CHUNKS.joinToString(\"\"))")
                appendLine("    }")
                appendLine("}")
            }
        file.writeText(source)
    }

    private fun generateSdfBytes(
        size: Int,
        control: Float,
        halfRange: Float,
        bezierSamples: Int,
    ): ByteArray {
        val handle = 1f - control
        val bx = FloatArray(bezierSamples + 1)
        val by = FloatArray(bezierSamples + 1)
        for (i in 0..bezierSamples) {
            val t = i.toFloat() / bezierSamples
            val omt = 1f - t
            bx[i] = 3f * omt * t * t * handle + t * t * t
            by[i] = omt * omt * omt + 3f * omt * omt * t * handle
        }
        val bytes = ByteArray(size * size)
        val invSize = 1f / size
        val invRange2 = 1f / (2f * halfRange)
        for (py in 0 until size) {
            val y = (py + 0.5f) * invSize
            for (px in 0 until size) {
                val x = (px + 0.5f) * invSize
                var minSqDist = Float.MAX_VALUE
                var closestIdx = 0
                for (i in 0 until bezierSamples) {
                    val ax = bx[i]
                    val ay = by[i]
                    val sx = bx[i + 1] - ax
                    val sy = by[i + 1] - ay
                    val len2 = sx * sx + sy * sy
                    if (len2 < 1e-12f) continue
                    var u = ((x - ax) * sx + (y - ay) * sy) / len2
                    if (u < 0f) {
                        u = 0f
                    } else if (u > 1f) {
                        u = 1f
                    }
                    val qx = ax + u * sx
                    val qy = ay + u * sy
                    val ddx = x - qx
                    val ddy = y - qy
                    val sqDist = ddx * ddx + ddy * ddy
                    if (sqDist < minSqDist) {
                        minSqDist = sqDist
                        closestIdx = i
                    }
                }
                val dist = sqrt(minSqDist)
                val tx = bx[closestIdx + 1] - bx[closestIdx]
                val ty = by[closestIdx + 1] - by[closestIdx]
                val pdx = x - bx[closestIdx]
                val pdy = y - by[closestIdx]
                val cross = tx * pdy - ty * pdx
                val signedDist = if (cross > 0f) -dist else dist
                val alpha = (0.5f - signedDist * invRange2).coerceIn(0f, 1f)
                bytes[py * size + px] = (alpha * 255f + 0.5f).toInt().toByte()
            }
        }
        return bytes
    }
}

val bakeSquircleSdf by tasks.registering(BakeSquircleSdfTask::class) {
    description = "bakeSquircleSdf"
    control.set(0.643f)
    size.set(512)
    halfRange.set(0.125f)
    bezierSamples.set(64)
    packageName.set("top.yukonga.miuix.kmp.squircle.internal")
    outputDir.set(layout.buildDirectory.dir("generated/baked-sdf"))
}

